public class IndiceCadena {
    public static void main(String[] args) {
        var cadena1 = "hola mundo";

        //Recuperar caracter
        var primerCadena = cadena1.charAt(0);
        System.out.println("primerCadena = " + primerCadena);
        var segundaCadena = cadena1.charAt(3);
        System.out.println("segundaCadena = " + segundaCadena);
        var TerceraCadena = cadena1.charAt(5);
        System.out.println("TerceraCadena = " + TerceraCadena);

        
    }
}
