public class SentenciaIf {
    public static void main(String[] args) {

        int edad = 15;

        if (edad >=18){
            System.out.println("Sos mayor de edad");
        }
        else if (edad >= 13 && edad <=18){
            System.out.println("Sos un adolescente");
        }
        else {
            System.out.println("Sos menor de edad");
        }


    }
}
